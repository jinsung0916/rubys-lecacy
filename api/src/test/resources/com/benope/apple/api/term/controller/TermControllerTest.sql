주식회사 베노프하우스(이하 ''당사'')가 인터넷과 모바일 등 Rubys 플랫폼서비스(이하 ‘서비스’)를 통해 제공하는 다양한 서비스를 이용하여 주셔서 감사합니다. 당사는 이용자가 서비스를 더 편리하게 이용할 수 있도록 서비스 이용약관을 마련하였습니다. 본 약관은 이용자가 서비스를 이용하는데 필요한 권리, 의무 및 책임사항 기타 필요한 사항을 규정하고 있으므로 주의 깊게 살펴봐 주시기 바랍니다.
제 2 조 (정의)
