2.서비스: 콘텐츠 및 그를 매개로 한 이용자 간 커뮤니케이션 공간, 파트너 재화 등의 광고 및 파트너와 고객 간 거래의 중개 등 이용자의 생활을 더 편리하게 하기 위해 당사가 Rubys를 통하여 제공 또는 제공할 서비스를 말합니다.
3.제휴서비스: 당사가 자신이 운영하는 타 서비스 또는 자신과 협력관계에 있는 제3자가 운영하는 서비스와 Rubys를 연동하는 등 다양한 제휴 방식을 통해 Rubys와 별개로 국내 ∙ 외에서 제공하는 서비스를 말합니다.
4.이용자: 당사가 제공하는 서비스를 이용하는 자로서 회원 및 비회원을 포함합니다.
5.회원: Rubys에 회원등록을 한 자로서, Rubys 내에서 서비스를 이용할 수 있는 자격을 갖춘 자를 말합니다.
6.비회원: 회원에 가입하지 않고, Rubys에서 제한적으로 제공하는 서비스를 이용하는 자를 말합니다.
7.콘텐츠: 당사 또는 이용자가 서비스 상에 게시 또는 등록하는 모든 글, 사진, 동영상, 각종 파일, 링크, 회원소개 등(부호, 문자, 영상, 그림 등 그 형태를 불문합니다) Rubys 내에서 게시 또는 사용되는 일체의 정보를 말합니다.
8.파트너: Rubys가 지정한 절차를 마치고 Rubys를 이용하는 자로, Rubys를 통해 이용자에게 자신의 재화 등을 광고 또는 판매하는 자를 말합니다.
9.회원정보: Rubys를 통해 가입을 신청한 자에게 회원가입 신청양식(이하 ''신청양식'')에 따라 기재를 요청하는 개인정보 및 회원의 식별과 서비스 이용을 위하여 회원이 Rubys에 제공한 정보를 말합니다.
제 3 조 (약관 등의 설명 및 개정)
1.Rubys는 본 약관의 내용과 상호 및 대표자 성명, 영업소 소재지 주소(소비자의 불만을 처리할 수 있는 주소를 포함), 전화번호, 전자우편주소, 사업자등록번호, 통신판매업신고번호 등을 이용자가 쉽게 알 수 있도록 Rubys 내 초기 서비스 화면(전면)에 게시합니다. 다만, 본 약관의 내용(개정시 개정내용을 포함)은 이용자가 연결 화면을 통하여 볼 수 있도록 게시할 수 있습니다.
2.Rubys는 필요한 경우 관련법령을 위배하지 않는 범위 내에서 본 약관을 개정할 수 있으며, 본 약관을 개정할 경우 그 개정이유 및 적용일자를 명시하여 현행 약관과 함께 Rubys내 공개된 공간에 그 적용일자 7일 전부터 적용일자 전일까지 공지합니다. 다만, Rubys가 회원에게 불리한 내용으로 약관을 변경하는 경우에는 최소한 30일 이상의 사전 유예 기간을 두고 공지하며, 이 경우 개정 전후의 내용을 회원이 알기 쉽도록 표시하겠습니다.
3.Rubys가 개정약관을 공지 및 통지하면서 회원에게 일정 기한 내에 의사표시를 하지 않으면 동의한 것으로 본다는 뜻을 명확하게 따로 공지 및 통지하였음에도 회원이 명시적으로 거부의 의사표시를 하지 아니한 경우, 회원이 개정약관에 동의한 것으로 봅니다. 또한, 회원이 개정약관의 적용에 동의하지 않는 경우 Rubys는 개정약관의 내용을 적용할 수 없으며, 이 경우, 회원은 회원탈퇴를 할 수 있습니다. 다만, 개정 전의 약관을 적용할 수 없는 특별한 사정이 있는 경우에는 Rubys는 회원자격을 상실시킬 수 있습니다.
4.Rubys가 본 약관을 개정할 경우 그 개정약관은 그 적용일자 이후에 체결되는 계약에만 적용되고 그 이전에 이미 체결된 계약에 대해서는 개정 전의 약관이 그대로 적용됩니다. 다만, 개정 전 약관 내용이 관련 법령에 위배되는 방식으로 적용될 여지가 있거나, 실제 적용되는 경우와 회원이 그 적용일자 이전에 체결된 계약에 대하여 개정약관의 적용을 받기 원하는 의사표시를 제3항에 의한 개정약관의 공지기간 내에 Rubys에 전달하여 동의를 받은 경우에는 개정약관이 적용됩니다.
제 4 조 (약관 외 준칙)
1.본 약관에서 정하지 아니한 사항과 본 약관의 해석에 관하여는 『전자상거래 등에서의 소비자보호에 관한 법률』, 『약관의 규제에 관한 법률』, 공정거래위원회가 정하는 『전자상거래 등에서의 소비자보호 지침』 등 관련법령 및 상관례에 따릅니다.
2.Rubys는 필요한 경우 특정 서비스에 관하여 적용될 사항(이하 ''개별약관'')을 정하여 이를 Rubys를 통해 미리 공지할 수 있습니다.
