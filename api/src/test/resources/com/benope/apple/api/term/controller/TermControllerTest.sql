2.회원의 과실로 인한 ID 또는 비밀번호의 유출, 기타 본 약관에 반하여 이루어진 회원의 ID 또는 비밀번호에 대한 양도, 대여 행위로 인하여 발생하는 손실이나 손해에 대하여는 Rubys의 귀책사유가 없는 한 회원 본인이 전적으로 책임을 부담하오니 유의하여 주시기 바랍니다.
3.Rubys는 회원 ID의 유출 우려가 있거나, ID에 반사회적 또는 미풍양속에 어긋나는 표현이 포함되어 있는 경우, 기타 이용자를 당사 및 Rubys의 운영자로 오인할 우려가 있는 경우에는 사전예고 없이 본 약관 제11조 제4항에 따라 회원자격을 제한하거나 해당 ID의 이용을 제한할 수 있습니다.
4.회원은 자신의 ID 및 비밀번호를 도난당하거나, 무단 사용을 발견하면, 이를 즉시 Rubys에 통보하여야 하며, Rubys는 통보를 받은 즉시 회원의 정보를 보호하기 위하여 필요한 최선의 노력을 다할 것입니다.
